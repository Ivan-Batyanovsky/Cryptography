def xor(x, dx):
    ret = []
    for i in range(len(x) - len(dx)):
        dx.append(0x00)
    dx.reverse()
    for tdx, tx in zip(dx, x):
        ret.append(hex(tx ^ tdx))
    return ret


def bitcounter(aS, bs):
    counter = 0
    # print(aS[0], bs[0])
    for a, b in zip(aS, bs):
        # print(a, b)
        counter += len([1 for lol in bin(a ^ b)[2:] if lol == "1"])
    return counter


